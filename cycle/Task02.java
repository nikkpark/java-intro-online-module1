package ru.hitchin.module1.cycle;

/*
 * Задача 2.
 * Необходимо вывести на экран числа от 5 до 1.
 */
public class Task02 {

    public static void main(String[] args) {
        task02();
    }
    
    public static void task02() {
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }
    }

}
