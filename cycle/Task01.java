package ru.hitchin.module1.cycle;

/*
 * Задача 1.
 * Необходимо вывести на экран числа от 1 до 5
 */
public class Task01 {

    public static void main(String[] args) {
        task01();
    }
    
    public static void task01() {
        for (int i = 1; i < 6; i++) {
            System.out.println(i);
        }
    }
}
